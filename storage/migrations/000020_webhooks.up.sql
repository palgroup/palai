GRANT SELECT, INSERT, UPDATE, DELETE ON webhook_endpoints, webhook_deliveries, delivery_attempts TO palai_app;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO palai_app;

